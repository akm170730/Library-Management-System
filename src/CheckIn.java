    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection conn=null;
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library1?autoReconnect=true&useSSL=false", "root","abhishek");
            Statement stmt = conn.createStatement();
            System.out.println(conn);
            stmt.execute("use library1;");
            if(jTextField2.getText().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid Loan Id");
                System.exit(1);
            }
            long loanid = Long.parseLong(jTextField2.getText());
            stmt.executeUpdate("update book_loans set date_in=curdate() where loan_id="+loanid+"");
            ResultSet rs1 = stmt.executeQuery("Select isbn from book_loans where loan_id="+loanid+"");
            long isbn=0;
            while(rs1.next())
            {isbn = rs1.getLong("isbn");}
            stmt.executeUpdate("update books set availability=1 where isbn="+isbn+"");
            ResultSet rs= stmt.executeQuery("select date_in, due_date from book_loans where loan_id="+loanid+";");
            Date duedate=null,datein=null;
            while(rs.next())
            {
               duedate = rs.getDate("due_date");
               datein = rs.getDate("date_in");
            }
            long datediff = (datein.getTime()-duedate.getTime())/(24 * 60 * 60 * 1000);
            System.out.println(datediff);
            if(datediff>0)
            {
                double fineamt = datediff*0.25;
                stmt.executeUpdate("insert into fines(loan_id,fine_amt) values ("+loanid+","+fineamt+");");
            }
            conn.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

