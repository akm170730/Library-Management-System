    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try 
        {
            Connection conn = null;
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library1?autoReconnect=true&useSSL=false", "root","abhishek");
            Statement stmt = conn.createStatement();
            System.out.println(conn);
            stmt.execute("use library1;");
            String fname = jTextField1.getText();
            if(fname.equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid First Name");
                System.exit(0);
            }
            String lname = jTextField2.getText();
            if(lname.equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid Last Name");
                System.exit(1);
            }
            String ssn = jTextField3.getText();
            if(ssn.equals(""))
            {
                //System.out.println("hello");
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid SSN");
                System.exit(2);
            }
            String address = jTextField4.getText();
            if(address.equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid Address");
                System.exit(2);
            }
            String phone = jTextField5.getText();
            String email = jTextField6.getText();
            ResultSet rs=stmt.executeQuery("Select ssn from borrower;");
            boolean flag = false;
            while(rs.next())
            {
                String ssnn = rs.getString("ssn");
                if(ssn.equals(ssnn))
                {
                    flag=true;
                    break;
                }
            }
            if(flag==true)
            {
                JOptionPane.showMessageDialog(rootPane, "Invalid SSN");
            }
            else
            {
                stmt.executeUpdate("insert into borrower (fname,lname,ssn,address,phone,email) values ('"+fname+"','"+lname+"','"+address+"','"+phone+"','"+email+"');");
                JOptionPane.showMessageDialog(rootPane, "Borrower Added");
            }           
            conn.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

