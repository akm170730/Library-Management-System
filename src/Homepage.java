    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection conn=null;
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/library1?autoReconnect=true&useSSL=false", "root","abhishek");
            Statement stmt = conn.createStatement();
            Statement stmt1 = conn.createStatement();
            System.out.println(conn);
            stmt.execute("use library1;");
            String search = jTextField1.getText();
            System.out.println(search);
            if(search.equals(""))
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter valid Search Input");
            }
            else
            {
                    ResultSet rs = stmt.executeQuery("select distinct b.isbn, b.title from books b, book_author ba, authors a where ba.Author_id=a.Author_id and b.isbn=ba.isbn and (b.isbn like '%"+search+"%' or a.name like '%"+search+"%' or b.title like '%"+search+"%');");
                    if(rs==null)
                    {
                         JOptionPane.showMessageDialog(this,"No Results Found ","Error",JOptionPane.ERROR_MESSAGE);
                    }
                //ResultSet rs = stmt.executeQuery("select * from books;");
                jTextArea1.append("Search Results\n");
                jTextArea1.append("____________________________________________________________________________________\n");
                while(rs.next())
                {
                    long isbn = rs.getLong("b.isbn");
                    String title = rs.getString("b.Title");
                    String joint_auth="";
                    ResultSet rs1 = stmt1.executeQuery("select a.Name from authors    a, books b, book_author ba where b.isbn=ba.isbn and ba.Author_id=a.Author_id and b.isbn="+isbn+";");
                    while(rs1.next())        
                    {
                        joint_auth=joint_auth+rs1.getString("a.name")+";";
                        
                    }
                    joint_auth=joint_auth.substring(0, joint_auth.length()-1);
                    jTextArea1.append("ISBN: "+isbn+"\n");
                    jTextArea1.append("Title: "+title+"\n");
                    jTextArea1.append("Author: "+joint_auth+"\n");
                    jTextArea1.append("____________________________________________________________________________________\n");
                }
            }        
            conn.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

